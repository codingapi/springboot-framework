export class Calculate{

    static add = (a: number, b: number) => {
        return a + b;
    }

    static sub = (a: number, b: number) => {
        return a - b;
    }

    static mul = (a: number, b: number) => {
        return a * b;
    }

    static div = (a: number, b: number) => {
        return a / b;
    }
}
